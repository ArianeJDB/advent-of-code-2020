function parseInput(inputText) {
    return inputText.trim().split("\n")
}

function resolve(input) {
    let result= 0;

    for(let i = 1; i < input.length; i++) {
        const widthStr = input[i].length
        let char = (i*3) % widthStr
        if(input[i][char] === "#") {
            result += 1
        }  
    }
    return result
}


